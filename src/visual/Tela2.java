    private void btnEnviarMensagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarMensagemActionPerformed
        // TODO add your handling code here:
        if(validaCampos()){
            
        String sql = "INSERT INTO chat VALUES (NULL, ? )";
        
        try  
        {
            PreparedStatement pst = Conexao.getPreparedStatement(sql);
            pst.setString(1, legendaLogin.getText()+": "+txtChat.getText());
            if(pst.executeUpdate()>0){
                JOptionPane.showMessageDialog(null, "Mensagem enviada com sucesso");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Mensagem não enviada");
            }
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Erro de SQL: "+ex.getMessage());
        }
        atualizarTabela();
        }
    }//GEN-LAST:event_btnEnviarMensagemActionPerformed

