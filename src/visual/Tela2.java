    public void atualizarTabela(){
        listChat.clear();
        listChat.addAll(daoChat.getListaChat());
        int linha = listChat.size()-1;
        
        if(linha>=0)
        
        {
        tabelaChat.setRowSelectionInterval(linha, linha);
        tabelaChat.scrollRectToVisible(tabelaChat.getCellRect(linha,linha,true));
        }
    }
    
    public boolean validaCampos(){
        
        if(!(txtChat.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Insira a mensagem");
            txtChat.requestFocus();
            return false;
        }
        
        if(txtChat.getText().length()>30){
            JOptionPane.showMessageDialog(null, "São muitos dados, por favor insira somente até 30 letras, números, pontos ou caracteres");
            txtChat.requestFocus(); 
            return false;
        }
        return true;
    }
    
